package com.company;

public class Main {

    public static void main(String[] args) {
        // primitive types of 8 types

        // case sensitive type language / strongly typed language = you need to define the type
        // declare a variable name number type of int
        // int range 2billion - +
        int number;
        // assign the variable number
        number = 5;

        System.out.println(number);

        boolean isOn = false;

        System.out.println(isOn);

        // range -128 127
        byte bNumber = 100;
        System.out.println(bNumber);

        // long type
        long bigNumber = 18291829182918291L;
        System.out.println(bigNumber);

        double pi = 3.14;
        System.out.println(pi);

        float price = 12.6f;
        System.out.println(price);

        char letter = 'q';

        String text = "Hello world!";
    }
}
